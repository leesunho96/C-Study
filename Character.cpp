Character::Character()
{
	cout << "기본생성자" << endl;
	name = "SGA";
	hp = 10;

}

Character::Character(string name, int hp)
	:name(name)
{
	cout << "파라미터 있는 생성자" << endl;

	this->name = name;
	this->hp = hp;

	(*this).name;


}

string Character::getname()
{
	return name;
}

Character::~Character()
{
	cout << "기본 소멸자 호출" << endl;
}
